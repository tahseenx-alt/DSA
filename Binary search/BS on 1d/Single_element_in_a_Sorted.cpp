//Find out how many times the array has been rotated

#include<iostream>
#include<vector>
using namespace std;


int main() {
    int n;
    cin >> n;
    
    vector<int> arr;
    for (int i = 0; i < n; i++) {
        int x;
        cin >> x;
        arr.push_back(x);
    }
    
    return 0;
}



